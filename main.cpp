// Function to display the inventory
void Inventory(string item1, string item2, string item3, string item4, string item5) {
    cout << "Your Inventory:" << endl;
    if (item1 != "") cout << "- " << item1 << endl;
    if (item2 != "") cout << "- " << item2 << endl;
    if (item3 != "") cout << "- " << item3 << endl;
    if (item4 != "") cout << "- " << item4 << endl;
    if (item5 != "") cout << "- " << item5 << endl;
    if (item1 == "" && item2 == "" && item3 == "" && item4 == "" && item5 == "") cout << "You have no items." << endl;
}

// Function to choose a character
string CharacterSelect() {
    string characterChoice;
    cout << "Choose your character:" << endl;
    cout << "1. Sea Horse" << endl;
    cout << "2. Puffer Fish" << endl;
    cout << "3. Crab" << endl;
    cout << "Enter 1, 2, 3,: ";
    getline(cin, characterChoice);

    if (characterChoice == "1") {
        return "Crab";
    }
    else if (characterChoice == "2") {
        return "Sea Horse";
    }
    else if (characterChoice == "3") {
        return "Puffer Fish";
    }
    else {
        cout << "Invalid choice, defaulting to Crab." << endl;
        return "Crab";
    }
}

